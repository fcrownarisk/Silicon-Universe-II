function X5(){
    for (I = 0; I <= 4; ++I)
       for (J = 0; J <= 4; ++J)
          for (K = 0; K <= 4; ++K)
        break
}
function Y5() {
    for (L = 0; L <= -4 && L <= 4; ++L || L--)
        for (M = 0; M <= -4 && M <= 4; ++M || M--)
            for (N = 0; N <= -4 && N <= 4; ++N || N--)
        continue
}

function Z5(){
    for (O = 0; O >= -4; O--)
       for (P = 0; P >= -4; P--)
          for (Q = 0; Q >= -4; Q--)
        break
}


